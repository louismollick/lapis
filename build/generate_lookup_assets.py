from __future__ import annotations

import json
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
BACK_TEMPLATE_PATH = REPO_ROOT / "src" / "back.html"
STYLING_CSS_PATH = REPO_ROOT / "src" / "styling.css"
OUTPUT_PATH = REPO_ROOT / "anki_addon" / "lapis_lookup" / "generated_lookup_assets.py"


def render_assignment(name: str, value: str) -> str:
    return f"{name} = {json.dumps(value, ensure_ascii=False)}\n"


def main() -> None:
    back_template = BACK_TEMPLATE_PATH.read_text(encoding="utf-8")
    styling_css = STYLING_CSS_PATH.read_text(encoding="utf-8")
    output = (
        "# Generated by build/generate_lookup_assets.py. Do not edit by hand.\n\n"
        + render_assignment("BACK_TEMPLATE", back_template)
        + "\n"
        + render_assignment("STYLING_CSS", styling_css)
    )
    OUTPUT_PATH.write_text(output, encoding="utf-8")


if __name__ == "__main__":
    main()
