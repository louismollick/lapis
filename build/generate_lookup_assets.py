from __future__ import annotations

import json
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
BACK_TEMPLATE_PATH = REPO_ROOT / "src" / "back.html"
STYLING_CSS_PATH = REPO_ROOT / "src" / "styling.css"
OUTPUT_PATH = REPO_ROOT / "anki_addon" / "lapis_lookup" / "generated_lookup_assets.py"

LOOKUP_CSS_START = "/* lapis-lookup-v1:start */"
LOOKUP_CSS_END = "/* lapis-lookup-v1:end */"
LOOKUP_MARKUP_START = "<!-- lapis-lookup-v1:markup:start -->"
LOOKUP_MARKUP_END = "<!-- lapis-lookup-v1:markup:end -->"
LOOKUP_SCRIPT_START = "// lapis-lookup-v1:script:start"
LOOKUP_SCRIPT_END = "// lapis-lookup-v1:script:end"


def render_assignment(name: str, value: str) -> str:
    return f"{name} = {json.dumps(value, ensure_ascii=False)}\n"


def extract_block(text: str, start_marker: str, end_marker: str) -> str:
    start = text.index(start_marker)
    end = text.index(end_marker, start) + len(end_marker)
    return text[start:end]


def main() -> None:
    back_template = BACK_TEMPLATE_PATH.read_text(encoding="utf-8")
    styling_css = STYLING_CSS_PATH.read_text(encoding="utf-8")
    lookup_markup = extract_block(back_template, LOOKUP_MARKUP_START, LOOKUP_MARKUP_END)
    lookup_script_source = extract_block(back_template, LOOKUP_SCRIPT_START, LOOKUP_SCRIPT_END)
    lookup_script_body = "\n".join(
        line
        for line in lookup_script_source.splitlines()
        if line.strip() not in {LOOKUP_SCRIPT_START, LOOKUP_SCRIPT_END}
    ).strip()
    lookup_script = (
        "<script>\n"
        "(() => {\n"
        "    if (window.__lapisLookupInitialized) return;\n"
        "    window.__lapisLookupInitialized = true;\n\n"
        f"{lookup_script_body}\n\n"
        "    initializeLookupOverlay();\n"
        "})();\n"
        "</script>"
    )
    lookup_css = extract_block(styling_css, LOOKUP_CSS_START, LOOKUP_CSS_END)
    output = (
        "# Generated by build/generate_lookup_assets.py. Do not edit by hand.\n\n"
        + render_assignment("LOOKUP_CSS_BLOCK", lookup_css)
        + "\n"
        + render_assignment("LOOKUP_MARKUP_BLOCK", lookup_markup)
        + "\n"
        + render_assignment("LOOKUP_SCRIPT_BLOCK", lookup_script)
    )
    OUTPUT_PATH.write_text(output, encoding="utf-8")


if __name__ == "__main__":
    main()
